#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*n;

	i = 0;
	while (s[i])
		i++;
	n = (char *)malloc(i + 1);
	if (!n)
		return (NULL);
	i = 0;
	while (s[i])
	{
		n[i] = s[i];
		i++;
	}
	n[i] = '\0';
	return (n);
}
